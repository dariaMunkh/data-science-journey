# Arithmetic and Math in Python
x = 3.14
y = -67
z = 11

# Rounding 
print(f"x: {x}")
print(f"Rounded x: {round(x)}")

# Absolute Value 
print(f"y: {y}")
print(f"Absolute value y: {abs(y)}")

# Power 
print(f"z: = {z}")
print(f"z to the power of 3: {pow(z, 3)}")

# Maximum
print(f"Max out of x, y, and z: {max(x,y,z)}")

# Minimum
print(f"Min out of x, y, and z: {min(x,y,z)}")
